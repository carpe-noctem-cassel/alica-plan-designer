package de.uni_kassel.vs.cn.planDesigner.codegeneration;

import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

/**
 * 
 */

/**
 * @author till
 * 
 */
public class CodeGenActivator extends AbstractUIPlugin {

	public static final String PLUGIN_ID = "de.uni_kassel.vs.cn.planDesigner.codegeneration";
	// The shared instance
	private static CodeGenActivator plugin;

	public CodeGenActivator() {
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * org.eclipse.ui.plugin.AbstractUIPlugin#start(org.osgi.framework.BundleContext
	 * )
	 */
	public void start(BundleContext context) throws Exception {
		System.out.println("CodeGenActivator.start(): called start");
		super.start(context);
		plugin = this;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * org.eclipse.ui.plugin.AbstractUIPlugin#stop(org.osgi.framework.BundleContext
	 * )
	 */
	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

	/**
	 * Returns the shared instance
	 * 
	 * @return the shared instance
	 */
	public static CodeGenActivator getDefault() {
		return plugin;
	}

}
